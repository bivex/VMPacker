func GenerateDynamicISA() {
	rand.Seed(time.Now().UnixNano())

	// Create a random permutation of 0..255
	perm := rand.Perm(256)

	for i, ptr := range opPtrs {
		randomByte := byte(perm[i])
		*ptr = randomByte
		GlobalOpMap[i] = randomByte
		InverseOpMap[randomByte] = byte(i)
	}
}
